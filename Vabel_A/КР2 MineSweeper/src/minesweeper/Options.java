    private void initRadioButton() {
        switch (mines) {
            case 10:
                currentLevel=LEVEL1;
                clearRadioButtons();
                this.jRadioButton1.setSelected(true);
                break;
            case 40:
                currentLevel=LEVEL2;
                clearRadioButtons();
                this.jRadioButton2.setSelected(true);
                break;
            case 90:
                currentLevel=LEVEL3;
                clearRadioButtons();
                this.jRadioButton3.setSelected(true);
                break;
            default:
                currentLevel=LEVEL4;
                this.jRadioButton4.setSelected(true);
                this.jTextField1.setText(String.valueOf(rows));
                this.jTextField2.setText(String.valueOf(cols));
                this.jTextField3.setText(String.valueOf(mines));
                break;
        }
    }


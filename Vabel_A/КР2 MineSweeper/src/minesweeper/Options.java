    private void handleInputValue() {
        try {
            int inputRows = Integer.valueOf(this.jTextField1.getText());
            if (validate(9, 24, inputRows)) {
                this.rows = inputRows;
            } else {
                error = true;
            }
            int inputCols = Integer.valueOf(this.jTextField2.getText());
            if (validate(9, 30, inputCols)) {
                this.cols = inputCols;
            } else {
                error = true;
            }
            int inputMines = Integer.valueOf(this.jTextField3.getText());
            if (validate(10, 668, inputMines)) {
                this.mines = inputMines;
            } else {
                error = true;
            }
            if (error) {
                JOptionPane.showMessageDialog(this, "Данные введены не правильно!");
            }
        } catch (Exception e) {
            error = true;
            JOptionPane.showMessageDialog(this, "Данные введены не правильно!");
        }
    }


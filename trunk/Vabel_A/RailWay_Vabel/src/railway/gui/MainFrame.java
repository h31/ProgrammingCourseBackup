    private void textareaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textareaMouseClicked
        // TODO add your handling code here:
        int selectionIndex = textarea.getAnchorSelectionIndex();
        RailwayStation selectedStation = listStations.get(selectionIndex);
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{
                    "Номер",
                    "Название",
                    "Прибытие",
                    "Отправление"});


        for (Train train : selectedStation.getTrains()) {
            String number = String.valueOf(train.getNumber());
            String name = train.getName();
            Time outcoming = null;
            
            Time incoming = null;
            if (train.getTrainPath().getStartPoint().getStation().getStationName().equals(selectedStation.getStationName())) {
                outcoming = train.getTrainPath().getStartPoint().getOutcomingTime();
            } else {
                if (!train.getTrainPath().getEndPoint().getStation().getStationName().equals(selectedStation.getStationName())) {
                    TrainPathItem pathItem = train.getTrainPath().searchItemByStationName(selectedStation.getStationName());
                    if (pathItem != null) {
                        outcoming = pathItem.getOutcomingTime();
                        incoming = pathItem.getIncomingTime();
                    }
                } else {
                    TrainPathItem item=train.getTrainPath().getEndPoint();
                    incoming = item.getIncomingTime();
                }
            }
            if (incoming != null) {
                if(outcoming!=null){
                    dtm.addRow(new String[]{number, name, incoming.toString(), outcoming.toString()});
                }else{
                    dtm.addRow(new String[]{number, name, incoming.toString(), null});
                }
            }else{
               dtm.addRow(new String[]{number, name, null, outcoming.toString()});
            }

        }

        jTable1.setModel(dtm);
    }//GEN-LAST:event_textareaMouseClicked


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectionIndex1 = jComboBox1.getSelectedIndex();
        int selectionIndex2 = jComboBox2.getSelectedIndex();
        String outcommingFrom = jTextField1.getText();
        String outcommingTo = jTextField2.getText();
        String incommingFrom = jTextField3.getText();
        String incommingTo = jTextField4.getText();
        if (selectionIndex1 == selectionIndex2) {
            JOptionPane.showMessageDialog(this, "Некорректный выбор станций", "Ошибка", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                RailwayStation station1 = listStations.get(selectionIndex1);
                RailwayStation station2 = listStations.get(selectionIndex2);
                SearchParams searchParams = new SearchParams(station1, station2, incommingFrom, outcommingFrom, incommingTo, outcommingTo);
                SimpleTravelPathSearcher searcher = new SimpleTravelPathSearcher(searchParams);
                List<TrevelPath> listTrevelPath = searcher.findTrevelPath();
                DefaultTableModel dtm = new DefaultTableModel();
                dtm.setColumnIdentifiers(new String[]{
                            "Поезд",
                            "Ст. отправления",
                            "Время отправления",
                            "Ст. назначения",
                            "Время назначения"});
                for (TrevelPath item : listTrevelPath) {
                    dtm.addRow(new String[]{item.getTrainName(),
                                item.getOutcomingItem().getStation().getStationName(),
                                item.getOutcomingItem().getOutcomingTime().toString(),
                                item.getIncomingItem().getStation().getStationName(),
                                item.getIncomingItem().getIncomingTime().toString()});
                }
                jTable2.setModel(dtm);
            } catch (Exception ex) {
                Logger.getLogger(DataLoader.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex, "Ошибка", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


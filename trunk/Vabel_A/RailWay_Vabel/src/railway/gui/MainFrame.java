    private void openFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileActionPerformed
        try {
            JFileChooser fileopen = new JFileChooser();

            int ret = fileopen.showDialog(null, "Открыть файл");
            if (ret == JFileChooser.APPROVE_OPTION) {
                File file = fileopen.getSelectedFile();

                listStations = DataLoader.parse(file);
                textarea.setListData(listStations.toArray());
                for (RailwayStation station : listStations) {
                    jComboBox1.addItem(station);
                    jComboBox2.addItem(station);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Произошла ошибка при чтении\\загрузке файла. Пожалуйста проверьте корректность данных", "Ошибка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_openFileActionPerformed

